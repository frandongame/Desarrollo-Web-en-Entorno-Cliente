//Creación de variables

var cadena1 = "Francisco";
var cadena2 = "Valcárcel Lardín";
var numero1 = 23;
var numero2 = 2003;

//Muestro los alerts con su valor

function alertVariables() {
    alert("Mi nombre es:\n" + cadena1 + " " + cadena2);
    alert("La suma de mi edad y mi año de nacimiento es: " + (numero1 + numero2));
    alert("La suma de todas mis variables es: " + (cadena1 + cadena2 + numero1 + numero2));
}

alertVariables();

